/** Get persona bio for a virtual model name. Falls back to a generic bio. */
export function getPersonaBio(model: string): string {
  return (
    PERSONAS[model]?.bio ??
    `${SYSTEM_PREAMBLE}\n\nТвоя роль: ассистент. Помоги пользователю с его задачей.`
  );
}
